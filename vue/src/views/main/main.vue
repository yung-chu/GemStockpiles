<style lang="less">
    @import "./main.less";
</style>

<template>
    <div class="main" :class="{'main-hide-text': shrink}">
        <div class="sidebar-menu-con" :style="{width: shrink?'60px':'200px', overflow: shrink ? 'visible' : 'auto'}">
            <scroll-bar ref="scrollBar">
                <shrinkable-menu 
                    :shrink="shrink"
                    @on-change="handleSubmenuChange"
                    :theme="menuTheme" 
                    :before-push="beforePush"
                    :open-names="openedSubmenuArr"
                    :menu-list="menuList">
                    <div slot="top" class="logo-con">
                        <img v-show="!shrink"  src="../../assets/images/logo.jpg" key="max-logo" />
                        <img v-show="shrink" src="../../assets/images/logo-min.jpg" key="min-logo" />
                    </div>
                </shrinkable-menu>
            </scroll-bar>
        </div>
        <div class="main-header-con" :style="{paddingLeft: shrink?'60px':'200px'}">
            <div class="main-header">
                <div class="navicon-con">
                    <Button :style="{transform: 'rotateZ(' + (this.shrink ? '-90' : '0') + 'deg)'}" type="text" @click="toggleClick">
                        <Icon type="navicon" size="32"></Icon>
                    </Button>
                </div>
                <div class="header-middle-con">
                    <div class="main-breadcrumb">
                        <breadcrumb-nav :currentPath="currentPath"></breadcrumb-nav>
                    </div>
                </div>
                <div class="header-avator-con">
                    <div class="user-dropdown-menu-con">
                        <Row type="flex" justify="end" align="middle" class="user-dropdown-innercon">
                            <Dropdown transfer trigger="click" @on-click="handleClickUserDropdown">
                                <a href="javascript:void(0)">
                                    <span class="main-user-name">{{ userName }}</span>
                                    <Icon type="arrow-down-b"></Icon>
                                </a>
                                <DropdownMenu slot="list">
                                    <DropdownItem name="ownspace">个人中心</DropdownItem>
                                    <DropdownItem name="loginout" divided>退出登录</DropdownItem>
                                </DropdownMenu>
                            </Dropdown>
                            <Avatar :src="avatorPath" style="background: #619fe7;margin-left: 10px;"></Avatar>
                        </Row>
                    </div>
                </div>
            </div>
            <div class="tags-con">
                <tags-page-opened :pageTagsList="pageTagsList"></tags-page-opened>
            </div>
        </div>
        <div class="single-page-con" :style="{left: shrink?'60px':'200px'}">
            <div class="single-page">
                <keep-alive :include="cachePage">
                    <router-view></router-view>
                </keep-alive>
            </div>
        </div>
    </div>
</template>

<script>
import shrinkableMenu from './components/shrinkable-menu/shrinkable-menu.vue';
import tagsPageOpened from './components/tags-page-opened.vue';
import breadcrumbNav from './components/breadcrumb-nav.vue';
import scrollBar from '@/components/scroll-bar/vue-scroller-bars';
import Util from '@/libs/util.js';

export default {
    components: {
        shrinkableMenu,
        tagsPageOpened,
        breadcrumbNav,
        scrollBar
    },
    data () {
        return {
            shrink: false,
            openedSubmenuArr: this.$store.state.app.openedSubmenuArr
        };
    },
    computed: {
        userName() {
            return this.$store.state.session.user ? this.$store.state.session.user.name : '';
        },
        menuList () {
            return this.$store.state.app.menuList;
        },
        pageTagsList () {
            return this.$store.state.app.pageOpenedList; //打开的页面的页面对象
        },
        currentPath () {
            return this.$store.state.app.currentPath; //当前面包屑数组
        },
        avatorPath () {
            return localStorage.avatorImgPath;
        },
        cachePage () {
            return this.$store.state.app.cachePage;
        },
        menuTheme () {
            return this.$store.state.app.menuTheme;
        }
    },
    methods: {
        init () {
            let pathArr = Util.setCurrentPath(this, this.$route.name);
            this.$store.commit('app/updateMenulist');
            if (pathArr.length >= 2) {
                this.$store.commit('app/addOpenSubmenu', pathArr[1].name);
            }
            this.checkTag(this.$route.name);
        },
        toggleClick () {
            this.shrink = !this.shrink;
        },
        handleClickUserDropdown (name) {
            if (name === 'ownspace') {
                Util.openNewPage(this, 'ownspace');
                this.$router.push({
                    name: 'ownspace'
                });
            } else if (name === 'loginout') {
                this.$store.commit('app/logout');
                Util.abp.auth.clearToken();
                location.reload();
            }
        },
        checkTag (name) {
            let openpageHasTag = this.pageTagsList.some(item => {
                if (item.name === name) {
                    return true;
                }
            });
            if (!openpageHasTag) { //  解决关闭当前标签后再点击回退按钮会退到当前页时没有标签的问题
                Util.openNewPage(this, name, this.$route.params || {}, this.$route.query || {});
            }
        },
        handleSubmenuChange (val) {
            // console.log(val)
        },
        beforePush (name) {
            return true;
        },
        scrollBarResize () {
            if (this.$refs.scrollBar) {
                this.$refs.scrollBar.resize();
            }
        }
    },
    watch: {
        '$route' (to) {
            this.$store.commit('app/setCurrentPageName', to.name);
            let pathArr = Util.setCurrentPath(this, to.name);
            if (pathArr.length > 2) {
                this.$store.commit('app/addOpenSubmenu', pathArr[1].name);
            }
            this.checkTag(to.name);
            localStorage.currentPageName = to.name;
        },
        openedSubmenuArr () {
            setTimeout(() => {
                this.scrollBarResize();
            }, 300);
        }
    },
    mounted () {
        this.init();
        window.addEventListener('resize', this.scrollBarResize);
    },
    created () {
        // 显示打开的页面的列表
        this.$store.commit('app/setOpenedList');
    },
    dispatch () {
        window.removeEventListener('resize', this.scrollBarResize);
    }
};
</script>
